<h2 id="training">Nos formations</h2>
<h3>Permis auto</h3>
<div class="center"><img src="template/css/images/voitures.jpg" alt="Voitures" title="Voitures"></div>
<p>Le permis auto se composent d'une épreuve théorique, le code et d'un examen pratique, la conduite.</p>
<h4>La conduite</h4>
</p>D'une durée de 25 minutes, l'objectif de l'examen est de vérifier que le candidat est en mesure d'effectuer la vérification d'un élément technique en relation avec la sécurité routière, à l'intérieur et à l'extérieur du véhicule et de réaliser deux manœuvres différentes, dont une obligatoirement en marche arrière.</li>
<h3>Permis moto</h3>
<div class="center"><img src="template/css/images/motos.jpg" alt="Motos" title="Motos"></div>
<p>Le Permis moto se composent d'une épreuve théorique, le code et de 2 examens pratiques, le plateau et la circulation.</p>
<h4>Le plateau</h4>
<p>Cette examen comprend 5 épreuves, la première consiste à déplacer la moto sans l’aide du moteur puis d'effectuer des vérifications techniques, la seconde est une épreuve de maîtrise de la moto à allure réduite, la troisième est également une épreuve de maîtrise de la moto mais à allure plus élevée et avec des tests de freinage, la quatrième épreuve est similaire à la précendante mais avec des tests d'évitement et enfin la dernière épreuve est une interrogation orale.</li>
<h4>La circulation</h4>
<p>D'une durée de 30 minutes, l'objectif de l'examen est de vérifier que le candidat a acquis les connaissances et les comportements nécessaires pour circuler en toute sécurité, sans gêner, sans surprendre et sans être surpris. L'épreuve se déroule sur des itinéraires variés comportant de l'agglomération et un circuit à allure soutenue.</p>

